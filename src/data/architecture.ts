export function getNode(id: string): ArchitectureNode {
  const node = architectureNodes.find((candidate) => candidate.id === id)
  if (!node) throw new Error(`Unknown architecture node: ${id}`)
  return node
}

export function connectedNodeIds(nodeId: string): Set<string> {
  const ids = new Set<string>([nodeId])
  architectureConnections.forEach((connection) => {
    if (connection.from === nodeId) ids.add(connection.to)
    if (connection.to === nodeId) ids.add(connection.from)
  })
  return ids
}
